package org.front;
//====================================================================================
//PIXELTAIL-AutoLaunch-AppLaunch
//为像素尾巴启动器提供图形化界面
//====================================================================================
//------------------------------------------------------------------------------------
//导入必要的依赖
//------------------------------------------------------------------------------------
import javafx.application.Application;
//------------------------------------------------------------------------------------
//声明AppLaunch公共类
//------------------------------------------------------------------------------------
public class AppLaunch{
    //--------------------------------------------------------------------------------
    //定义主方法
    //--------------------------------------------------------------------------------
    public static void main(String[] args) {
        //从App类启动JavaFX窗口
        Application.launch(App.class, args);
    }
}